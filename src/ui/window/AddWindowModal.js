/**
 * Create a modal window used for adding item
 * @interface AddWindowModal
 */

/**
 * Triggered before an add event
 * @method onBeforeAdd
 */

/**
 * Add an item
 * @method onAdd
 * @param {Object} item - Item to be added
 */

/**
 * Triggered after an add event
 * @method onAfterAdd
 */

/**
 * Triggered before a show event
 * @method onBeforeShow
 */

/**
 * Show the modal window
 * @method show
 */

/**
 * Triggered after a show event
 * @method onAfterShow 
 */

/**
 * Triggered before a hide event
 * @method onBeforeHide
 */

/**
 * Hide the modal window
 * @method hide
 */

/**
 * Triggered after a hide event
 * @method onAfterHide 
 */
